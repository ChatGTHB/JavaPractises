package _14_ForLoop;

public class _01_Example {
    public static void main(String[] args) {
        /**
         Javada 1'den 10'a kadar olan sayıları ekrana yazdıran
         bir for döngüsü oluşturunuz.
         */

        for (int i = 1; i < 11; i++) {
            System.out.println(i);
        }
    }
}
